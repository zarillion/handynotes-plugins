local SturdyChestCriteria = {
    id = 1,
    qty = true,
    suffix = L['sturdy_chest_suffix']
}

local SturdyChest = Class('SturdyChest', Treasure, {
    icon = 'chest_yw',
    label = L['sturdy_chest'],
    scale = 1.25,
    getters = {
        rewards = function(self)
            return {
                Achievement({
                    id = self.achievementID,
                    criteria = SturdyChestCriteria
                })
            }
        end
    }
}) -- Sturdy Chest

